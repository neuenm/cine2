<?php

namespace CineBundle\Entity;

/**
 * Pelicopia
 */
class Pelicopia
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var \CineBundle\Entity\Formato
     */
    private $idFormato;

    /**
     * @var \CineBundle\Entity\Idioma
     */
    private $idioma;

    /**
     * @var \CineBundle\Entity\Pelicula
     */
    private $idPeli;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set idFormato
     *
     * @param \CineBundle\Entity\Formato $idFormato
     *
     * @return Pelicopia
     */
    public function setIdFormato(\CineBundle\Entity\Formato $idFormato = null)
    {
        $this->idFormato = $idFormato;

        return $this;
    }

    /**
     * Get idFormato
     *
     * @return \CineBundle\Entity\Formato
     */
    public function getIdFormato()
    {
        return $this->idFormato;
    }

    /**
     * Set idioma
     *
     * @param \CineBundle\Entity\Idioma $idioma
     *
     * @return Pelicopia
     */
    public function setIdioma(\CineBundle\Entity\Idioma $idioma = null)
    {
        $this->idioma = $idioma;

        return $this;
    }

    /**
     * Get idioma
     *
     * @return \CineBundle\Entity\Idioma
     */
    public function getIdioma()
    {
        return $this->idioma;
    }

    /**
     * Set idPeli
     *
     * @param \CineBundle\Entity\Pelicula $idPeli
     *
     * @return Pelicopia
     */
    public function setIdPeli(\CineBundle\Entity\Pelicula $idPeli = null)
    {
        $this->idPeli = $idPeli;

        return $this;
    }

    /**
     * Get idPeli
     *
     * @return \CineBundle\Entity\Pelicula
     */
    public function getIdPeli()
    {
        return $this->idPeli;
    }
    /**
     * @var string
     */
    private $estado;


    /**
     * Set estado
     *
     * @param string $estado
     *
     * @return Pelicopia
     */
    public function setEstado($estado)
    {
        $this->estado = $estado;

        return $this;
    }

    /**
     * Get estado
     *
     * @return string
     */
    public function getEstado()
    {
        return $this->estado;
    }
}
